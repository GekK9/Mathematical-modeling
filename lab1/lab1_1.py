import numpy as np

#  5(A^T - 2B)(C + 2D)
A = np.array([[0, 1, -2], [3, 2, 1], [2, 0 , -2], [1, 4, -1]])
B = np.array([[2, 1, 2, -2], [0, 3, -1, -1], [4, 1, 0, -3]])
C = np.array([[-3, 4], [-2, 1], [-1, 4], [-2, 3]])
D = np.array([[2, -2], [3, -4], [4, -3], [5, -4]])

print(5*np.dot((A.transpose() - 2 * B),(C + 2 * D)))